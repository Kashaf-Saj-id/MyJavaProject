package InheritanceCalculatorPackage;

/**
 * Advanced class that extends Basic class, adding multiplication and division operations.
 */
class Advnace extends Basic {
    // Method for multiplication
    public double multi(int a, int b) {
        return a * b;
    }

    // Method for division
    public double div(int a, int b) {
        return a / b;
    }
}
